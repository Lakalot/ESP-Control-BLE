// The normalized index/id values are copied from the spike's proven output:
//   StringTable (first-use order):
//     0 ""              1 "layout.sections"   2 "light.brightness"  3 "Brightness"
//     4 "%"             5 "light.set_brightness"  6 "Set Brightness"  7 <inputSchema JSON>
//     8 "home"          9 "Home"             10 "home.root"        11 "home.slider"
//    12 "percent"
//   ids (per kind, sort-by-id ascending, 1-based):
//     resources: light.brightness=1 ; actions: light.set_brightness=1 ;
//     screens: home=1 ; nodes: home.root=1, home.slider=2

