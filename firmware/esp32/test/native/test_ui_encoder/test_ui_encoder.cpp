// Build the UiModel for tiny.yaml, with string indices + ids already resolved to
// the spike's known-correct normalized values. (Task 4's EmitterUi will derive these
// from a fluent description; here the test pins the encoder against the oracle.)
static UiModel buildTinyFixtureModel() {
  UiModel m;
  m.version = 5;
  m.schemaVersion = 1;
  m.minAppVersion = "1.0.0";
  m.hasAppShell = false;

  // Intern strings in normalize.ts traversal order so indices match the oracle.
  const uint32_t sLayout   = m.strings.intern("layout.sections");      // 1
  const uint32_t sResSlug  = m.strings.intern("light.brightness");     // 2
  const uint32_t sResLabel = m.strings.intern("Brightness");           // 3
  const uint32_t sResUnit  = m.strings.intern("%");                    // 4
  const uint32_t sActSlug  = m.strings.intern("light.set_brightness"); // 5
  const uint32_t sActLabel = m.strings.intern("Set Brightness");       // 6
  const uint32_t sSchema   = m.strings.intern(
      "{\"type\":\"object\",\"additionalProperties\":false,\"required\":[\"value\"],"
      "\"properties\":{\"value\":{\"type\":\"integer\",\"minimum\":0,\"maximum\":100}}}"); // 7
  const uint32_t sScrSlug  = m.strings.intern("home");                 // 8
  const uint32_t sScrTitle = m.strings.intern("Home");                 // 9
  const uint32_t sRootSlug = m.strings.intern("home.root");            // 10
  const uint32_t sSlidSlug = m.strings.intern("home.slider");          // 11
  const uint32_t sFormat   = m.strings.intern("percent");              // 12

  // capabilities: required [layout.sections]
  m.featureIdxs.push_back(sLayout);

  // resource light.brightness (id 1): uint / subscribe / staleAfter 5000
  {
    UiResource r;
    r.id = 1;
    r.slugIdx = sResSlug;
    r.labelIdx = sResLabel;
    r.unitIdx = sResUnit;
    r.valueType = 3;   // VALUE_TYPE_UINT
    r.readMode = 2;    // READ_MODE_SUBSCRIBE
    r.staleAfterMs = 5000;
    r.pollMs = 0;
    m.resources.push_back(r);
  }

  // action light.set_brightness (id 1): normal / inputSchema set
  {
    UiAction a;
    a.id = 1;
    a.slugIdx = sActSlug;
    a.labelIdx = sActLabel;
    a.dangerLevel = 1; // DANGER_LEVEL_NORMAL
    a.confirmIdx = 0;
    a.cooldownMs = 0;
    a.inputSchemaIdx = sSchema;
    a.resultSchemaIdx = 0;
    m.actions.push_back(a);
  }

  // screen home (id 1): root node = home.root (node id 1)
  {
    UiScreen s;
    s.id = 1;
    s.slugIdx = sScrSlug;
    s.titleIdx = sScrTitle;
    s.routeKeyIdx = 0;
    s.rootNodeId = 1;
    m.screens.push_back(s);
  }

  // node home.root (id 1): STACK, child = home.slider (node id 2)
  {
    UiNode n;
    n.id = 1;
    n.slugIdx = sRootSlug;
    n.kind = 1;        // NODE_KIND_STACK
    n.widgetKind = 0;
    n.titleIdx = 0;
    n.toneIdx = 0;
    n.childrenIds.push_back(2);
    n.columns = 0;
    n.bindResourceId = 0;
    n.bindActionId = 0;
    n.textIdx = 0;
    n.formatHintIdx = 0;
    m.nodes.push_back(n);
  }

  // node home.slider (id 2): WIDGET/SLIDER, bind res 1 + act 1, formatHint percent
  {
    UiNode n;
    n.id = 2;
    n.slugIdx = sSlidSlug;
    n.kind = 5;        // NODE_KIND_WIDGET
    n.widgetKind = 5;  // WIDGET_KIND_SLIDER
    n.titleIdx = sResLabel; // "Brightness"
    n.toneIdx = 0;
    n.columns = 0;
    n.bindResourceId = 1;
    n.bindActionId = 1;
    n.textIdx = 0;
    n.formatHintIdx = sFormat;
    m.nodes.push_back(n);
  }

  return m;
}

static void test_tiny_manifest_encodes_byte_identical_to_oracle() {
  UiModel m = buildTinyFixtureModel();
  std::vector<uint8_t> out;
  TEST_ASSERT_TRUE(encodeUiModel(m, out));
  TEST_ASSERT_EQUAL_UINT32((uint32_t)sizeof(kOracle), (uint32_t)out.size());
  TEST_ASSERT_EQUAL_UINT8_ARRAY(kOracle, out.data(), sizeof(kOracle));
}

int main(int, char**) {
  UNITY_BEGIN();
  RUN_TEST(test_tiny_manifest_encodes_byte_identical_to_oracle);
  return UNITY_END();
}
