//
// buildTinyFixtureModel() reproduces the NORMALIZED form of spike/ui_emitter/tiny.yaml
// (1 resource, 1 action, 1 view = synthetic STACK root node + child slider). The
// encoder must produce bytes IDENTICAL to the TS toolchain's oracle.pb (420 bytes),
// which is embedded below as kOracle to avoid any CWD/file-path fragility in the
// native test runner.
