// Describe tiny.yaml via the fluent Ui API. The action's inputSchema is DERIVED
// from the slider widget + its [min,max] range (no hand-written JSON here).
static void describeTiny(Ui& ui) {
  ui.requireCapability("layout.sections");

  ResourceRef brightness = ui.resource("light.brightness", ValueType::Uint)
                               .label("Brightness")
                               .unit("%")
                               .readMode(ReadMode::Subscribe)
                               .staleAfterMs(5000);

  ui.view("home", "Home").content(
      ui.slider("home.slider", "Brightness", brightness, /*min*/ 0, /*max*/ 100)
          .action("light.set_brightness", "Set Brightness")  // schema derived from slider+range
          .formatHint("percent")
          .onSet([](uint8_t) {}));  // typed handler -- compiles/type-checks, ignored by EmitterUi
}

static void test_fluent_api_emits_oracle_bytes() {
  EmitterUi emitter;
  describeTiny(emitter);
  UiModel m = emitter.build();

  std::vector<uint8_t> out;
  TEST_ASSERT_TRUE(encodeUiModel(m, out));
  TEST_ASSERT_EQUAL_UINT32((uint32_t)sizeof(kOracle), (uint32_t)out.size());
  TEST_ASSERT_EQUAL_UINT8_ARRAY(kOracle, out.data(), sizeof(kOracle));
}

int main(int, char**) {
  UNITY_BEGIN();
  RUN_TEST(test_fluent_api_emits_oracle_bytes);
  return UNITY_END();
}
